<h6>1. What is your design service and how will it benefit me?</h6>
<p>We provide a professional graphic design and advertising design resource to our clients. Carbon Creative professionals have over 15 years experience working on a wide range of design sectors including corporate, bluechip, retail, advertising and government bodies. We design for all mediums, taking into consideration future brand growth needs. Some of the most common design solutions include advertising, brand logo / visual identity, brochure, exhibition, packaging, direct mail, marketing, stationery and website design. Your company will benefit from having experienced design professionals applying intelligent marketing and design solutions to answer the brief presented.</p>
<h6>2. Surely I can do all my own design work in Microsoft Word or Microsoft Publisher?</h6>
<p>It is often counter productive to attempt to design your own company literature, website etc. A good graphic design agency will have professional experienced personnel that will think beyond the job in hand and offer an objective view on the project. Carbon Creative strives to develop a concept for every job, not just produce something graphically interesting. Microsoft Word and Publisher are also not always user-friendly for printers, we ensure every file created is of high resolution and of a press ready format.</p>
<h6>3. If I commission a design project who owns the copyright and intellectual property rights?</h6>
<p>Unlike some other agencies we do not charge high release fees for our work, if work is completed at our standard rates of pay and intellectual property is requested before we start the project then on receipt of final payment we release all intellectual property rights on the design. We reserve the right to feature the work in our portfolio and our own self promotional material and that if our work is featured elsewhere such as within the scope of a another design or artwork we would expect to be credited.</p>
<h6>4. Where do we start and how do you involve your clients in the design process?</h6>
<p>We always start by thoroughly discussing the details of the project with the client. Essentially we call this the design brief, it can be verbally communicated through a meeting. We encourage the client to contribute ideas and samples of items and colours that are liked. Equally important is to understand concepts that the client doesn’t like. When we are satisfied we have enough information to work with, we will begin the project by researching ideas, considering concepts and then laying out design visuals, these will be forwarded to the client for feedback. We listen carefully to our clients feedback and make the necessary changes to the layouts and offer advice on why we arrived at the design conclusions submitted. This process of amendment and resubmission continues until the design naturally evolves into its final form.</p>
<h6>5. What if I don’t like what you design?</h6>
<p>This is rare. Sometimes we hit the nail on the head first time, sometimes it can take a number of discussions and design submissions before the client is happy with the concept. Ultimately we always ensure our clients walk away satisfied and we feel comfortable that we have put out a job to our highest standards within the confines of the timescale, brief and budget provided.</p>
